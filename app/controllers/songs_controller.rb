class SongsController < ApplicationController
  require 'csv'

  def upload
    CSV.foreach(params[:file].path, headers:true) do |item|
      Song.create(title: item[0], release_year: item[2], first: item[4], play_count: item[6], f_g: item[7], artist_name: item[1] )
    end
    redirect_to songs_path
  end
  def index
    @songs = Song.all
  end

  def show
    @song = Song.find(params[:id])
  end

  def new
    @song = Song.new
  end

  def create
    @song = Song.new(song_params)

    if @song.save
      redirect_to @song
    else
      render :new
    end
  end

  def edit
    @song = Song.find(params[:id])
  end

  def update
    @song = Song.find(params[:id])

    @song.update(song_params)

    if @song.save
      redirect_to @song
    else
      render :edit
    end
  end

  def destroy
    @song = Song.find(params[:id])
    @song.destroy
    flash[:notice] = "Song deleted."
    redirect_to songs_path
  end

  private

  def song_params
    params.require(:song).permit(:title, :artist_name)
  end
end
